package chapter03;

public class Types {
    //В java 8 примитивных типов данных поделенных на 4 группы:
    //region Целые_числа
    byte aByte; //от -128 до 127 (8 бит)
    short aShort; //от -32768 до 32767 (16 бит)
    int anInt; //от -21474883648 до 21474883647 (32 бит)
    long aLong; //от -9223372036854775808 до 9223372036854775807 (64 бит)
    //endregion
    //region Символы
    char aChar; //(16 бит)
    //endregion
    //region Плавающая_точка
    float aFloat; //(32 бит)
    double aDouble; //(64 бит)
    //endregion
    //region Логистический
    boolean aBoolean; //(32 бит)
    //endregion

}
